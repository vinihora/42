#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*new;
	int		i;
	int		count;

	if (!s1 || !s2)
		return (NULL);
	i = 0;
	count = 0;
	new = (char *)malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (!new)
		return (NULL);
	while (s1[i] != '\0')
	{
		new[i] = s1[i];
		i++;
	}
	while (s2[count] != '\0')
	{
		new[i + count] = s2[count];
		count++;
	}
	new[i + count] = '\0';
	return (new);
}
