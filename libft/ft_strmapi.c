#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*string;
	int				i;

	if (!s || !f)
	{
		return (NULL);
	}
	string = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!(string))
		return (NULL);
	i = 0;
	while (s[i])
	{
		string[i] = f(i, s[i]);
		i++;
	}
	string[i] = '\0';
	return (string);
}
